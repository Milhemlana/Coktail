
package mycoktail;
public class Color {
    private String r;
    private String g;
    private String b;
    public Color(){}
    public Color(String r,String b,String g){
        this.r=r;
        this.g=g;
        this.b=b;
    }

    public String getR() {
        return r;
    }

    public void setR(String r) {
        this.r = r;
    }

    public String getG() {
        return g;
    }

    public void setG(String g) {
        this.g = g;
    }

    public String getB() {
        return b;
    }

    public void setB(String b) {
        this.b = b;
    }
  
    
}
